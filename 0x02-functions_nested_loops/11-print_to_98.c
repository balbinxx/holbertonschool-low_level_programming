#include "holberton.h"
/**
 * print_to_98 - prints all natural numbers from n to 98
 * @n: integer
 *
 * Return: None
 */
void print_to_98(int n)
{
	int i = n;

	if (i <= 98);

	for (i = n; i <= 98; i++)
	{
		printf("%i, " , i);
	}
	if (i > 98);

	for (i = n; i >= 98; i--)
	{
		printf("%i, " , i);
	}
}
